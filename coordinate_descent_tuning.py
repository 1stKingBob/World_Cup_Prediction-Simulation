#!/usr/bin/env python3.10
"""
Coordinate-Descent Weight Tuning
=================================
Unlike backtest_weights.py's joint random search (which varies everything
at once and can produce confounded, hard-to-interpret results — see the
W_H2H=0.464 red herring), this sweeps ONE weight (or tightly-linked group)
at a time, holding everything else fixed at its current value, and keeps
whichever value minimizes Brier score on real WC 2018+2022 data. Cycles
through all groups repeatedly until a full pass makes no improvement
(coordinate descent to a local optimum).

Scope: the model's blend/weighting parameters — gap-combination weights,
the static-GD/player-performance split, the historical-vs-current curve,
H2H tier + decay parameters, historical-score decay, and K_SIG. Not
touched: more structural constants (ALPHA_HOME, ALPHA_STAKES, K_REL,
DEPTH_DECAY_RATE, STATIC_GD_ANCHORS, MATCHUP_MATRIX, per-player
RECENCY_WEIGHTS) — those are domain-design choices, not simple blend
weights, and are out of scope for this pass.

Usage: python3.10 coordinate_descent_tuning.py
"""

import importlib.util
import os

HERE = os.path.dirname(os.path.abspath(__file__))
DATA_DIR = os.path.join(HERE, "data")
YEARS = [2002, 2006, 2010, 2014, 2018, 2022]

spec = importlib.util.spec_from_file_location("wc_predictor", os.path.join(HERE, "wc_predictor (1).py"))
wcp = importlib.util.module_from_spec(spec)
spec.loader.exec_module(wcp)


DATASET = None  # loaded once in main() via wcp.load_backtest_dataset()


def evaluate():
    brier, _ = wcp.evaluate_brier(DATA_DIR, YEARS, dataset=DATASET)
    return brier


def set_simplex(names, values):
    total = sum(values)
    for n, v in zip(names, values):
        setattr(wcp, n, v / total)


def get_simplex(names):
    return [getattr(wcp, n) for n in names]


def sweep_simplex_dim(names, idx, candidates, label):
    """Sweep names[idx] over `candidates`, rescaling the other entries in
    the group proportionally (preserving their relative ratios) so the
    whole group still sums to 1. Keeps whichever candidate minimizes Brier."""
    base = get_simplex(names)
    others_idx = [i for i in range(len(names)) if i != idx]
    others_base = [base[i] for i in others_idx]
    others_sum = sum(others_base) or 1.0

    best_value = base[idx]
    best_brier = evaluate()
    best_group = base

    for v in candidates:
        remaining = 1.0 - v
        trial = list(base)
        trial[idx] = v
        for i in others_idx:
            trial[i] = base[i] / others_sum * remaining
        set_simplex(names, trial)
        b = evaluate()
        if b < best_brier:
            best_value, best_brier, best_group = v, b, trial

    set_simplex(names, best_group)
    print(f"  {label} ({names[idx]}): {base[idx]:.3f} -> {best_value:.3f}   Brier={best_brier:.4f}")
    return best_brier


def sweep_scalar(attr_name, candidates, label):
    base = getattr(wcp, attr_name)
    best_value, best_brier = base, evaluate()
    for v in candidates:
        setattr(wcp, attr_name, v)
        b = evaluate()
        if b < best_brier:
            best_value, best_brier = v, b
    setattr(wcp, attr_name, best_value)
    print(f"  {label} ({attr_name}): {base:.3f} -> {best_value:.3f}   Brier={best_brier:.4f}")
    return best_brier


def sweep_hist_curve(floor_candidates, power_candidates):
    """Reparametrize the 8-point HIST_WEIGHTS/CURR_WEIGHTS curve as
    HIST[i] = floor + (1-floor) * (1 - i/7)^power, then coordinate-sweep
    floor and power (2 scalar dims instead of 8 independent numbers)."""
    n = len(wcp.HIST_WEIGHTS)

    def build(floor, power):
        hist = [floor + (1 - floor) * (1 - i / (n - 1)) ** power for i in range(n)]
        curr = [1 - h for h in hist]
        return hist, curr

    # infer current (floor, power) isn't exact since the original curve was
    # hand-picked, not generated by this formula — start from a reasonable
    # floor (last value) and power=1 (linear), and let the sweep find better.
    floor0 = wcp.HIST_WEIGHTS[-1]
    power0 = 1.0
    best_floor, best_power = floor0, power0
    hist, curr = build(floor0, power0)
    wcp.HIST_WEIGHTS, wcp.CURR_WEIGHTS = hist, curr
    best_brier = evaluate()

    for floor in floor_candidates:
        hist, curr = build(floor, best_power)
        wcp.HIST_WEIGHTS, wcp.CURR_WEIGHTS = hist, curr
        b = evaluate()
        if b < best_brier:
            best_floor, best_brier = floor, b
    hist, curr = build(best_floor, best_power)
    wcp.HIST_WEIGHTS, wcp.CURR_WEIGHTS = hist, curr

    for power in power_candidates:
        hist, curr = build(best_floor, power)
        wcp.HIST_WEIGHTS, wcp.CURR_WEIGHTS = hist, curr
        b = evaluate()
        if b < best_brier:
            best_power, best_brier = power, b
    hist, curr = build(best_floor, best_power)
    wcp.HIST_WEIGHTS, wcp.CURR_WEIGHTS = hist, curr

    print(f"  HIST/CURR curve: floor={best_floor:.3f} power={best_power:.3f}   Brier={best_brier:.4f}")
    print(f"    HIST_WEIGHTS = {[round(h,3) for h in hist]}")
    return best_brier


def sweep_h2h_tier(candidates_wc, candidates_fr):
    base_wc = wcp.H2H_TIER_WEIGHTS["world_cup"]
    base_fr = wcp.H2H_TIER_WEIGHTS["friendly"]
    best_wc, best_brier = base_wc, evaluate()
    for v in candidates_wc:
        wcp.H2H_TIER_WEIGHTS["world_cup"] = v
        b = evaluate()
        if b < best_brier:
            best_wc, best_brier = v, b
    wcp.H2H_TIER_WEIGHTS["world_cup"] = best_wc

    best_fr = base_fr
    for v in candidates_fr:
        wcp.H2H_TIER_WEIGHTS["friendly"] = v
        b = evaluate()
        if b < best_brier:
            best_fr, best_brier = v, b
    wcp.H2H_TIER_WEIGHTS["friendly"] = best_fr

    print(f"  H2H_TIER_WEIGHTS: world_cup {base_wc:.2f}->{best_wc:.2f}, "
          f"friendly {base_fr:.2f}->{best_fr:.2f}   Brier={best_brier:.4f}")
    return best_brier


def main():
    global DATASET
    print("Loading backtest dataset once (2018 + 2022 CSVs)...")
    DATASET = wcp.load_backtest_dataset(DATA_DIR, YEARS)
    print("Done.\n")

    print("Coordinate-descent weight tuning on real WC 2018+2022 data")
    print("=" * 70)
    start_brier = evaluate()
    print(f"Starting Brier (current committed weights): {start_brier:.4f}\n")

    gap_names = ["W_BASE", "W_TAC", "W_H2H", "W_REL_GD"]
    layer1_names = ["WEIGHT_STATIC_GD", "WEIGHT_PLAYER_PERF"]

    frac_grid = [0.05, 0.10, 0.15, 0.20, 0.25, 0.30, 0.35, 0.40, 0.45,
                 0.50, 0.55, 0.60, 0.65, 0.70]

    prev_brier = start_brier
    for cycle in range(1, 4):
        print(f"--- Cycle {cycle} ---")

        for i, name in enumerate(gap_names):
            sweep_simplex_dim(gap_names, i, frac_grid, f"gap weight")

        sweep_simplex_dim(layer1_names, 0, frac_grid, "layer1 split")

        sweep_hist_curve(
            floor_candidates=[0.0, 0.05, 0.10, 0.15, 0.20, 0.25, 0.30, 0.35],
            power_candidates=[0.5, 0.75, 1.0, 1.25, 1.5, 1.75, 2.0, 2.5, 3.0],
        )

        sweep_scalar("K_SIG", [0.6, 0.8, 1.0, 1.1, 1.2, 1.3, 1.4, 1.5, 1.6, 1.8, 2.0], "sigmoid steepness")

        sweep_h2h_tier(
            candidates_wc=[1.0, 1.1, 1.2, 1.3, 1.4, 1.5, 1.6, 1.8, 2.0],
            candidates_fr=[0.3, 0.4, 0.5, 0.6, 0.7, 0.8, 0.9, 1.0],
        )

        sweep_scalar("H2H_MAX_AGE_YEARS", [2, 3, 4, 5, 6, 7, 8], "H2H lookback window (years)")
        sweep_scalar("H2H_DECAY_POWER", [0.5, 0.75, 1.0, 1.25, 1.5, 1.75, 2.0, 2.5], "H2H decay shape")

        sweep_scalar("HIST_DECAY_MAX_YEARS", [8, 10, 12, 14, 16, 20, 24], "historical-WC decay window")
        sweep_scalar("HIST_DECAY_POWER", [0.5, 0.75, 1.0, 1.25, 1.5, 1.75, 2.0, 2.5], "historical-WC decay shape")

        cycle_brier = evaluate()
        print(f"  --> end of cycle {cycle}: Brier = {cycle_brier:.4f}\n")

        if prev_brier - cycle_brier < 0.0005:
            print("Converged (negligible improvement this cycle).\n")
            break
        prev_brier = cycle_brier

    final_brier = evaluate()
    print("=" * 70)
    print("FINAL RESULT")
    print("=" * 70)
    print(f"Start:  {start_brier:.4f}")
    print(f"Final:  {final_brier:.4f}   ({(start_brier-final_brier)/start_brier*100:+.1f}%)")
    print()
    print("Final values:")
    for n in gap_names:
        print(f"  {n} = {getattr(wcp, n):.4f}")
    for n in layer1_names:
        print(f"  {n} = {getattr(wcp, n):.4f}")
    print(f"  HIST_WEIGHTS = {[round(x,4) for x in wcp.HIST_WEIGHTS]}")
    print(f"  CURR_WEIGHTS = {[round(x,4) for x in wcp.CURR_WEIGHTS]}")
    print(f"  K_SIG = {wcp.K_SIG:.4f}")
    print(f"  H2H_TIER_WEIGHTS = {wcp.H2H_TIER_WEIGHTS}")
    print(f"  H2H_MAX_AGE_YEARS = {wcp.H2H_MAX_AGE_YEARS}")
    print(f"  H2H_DECAY_POWER = {wcp.H2H_DECAY_POWER:.4f}")
    print(f"  HIST_DECAY_MAX_YEARS = {wcp.HIST_DECAY_MAX_YEARS}")
    print(f"  HIST_DECAY_POWER = {wcp.HIST_DECAY_POWER:.4f}")


if __name__ == "__main__":
    main()
